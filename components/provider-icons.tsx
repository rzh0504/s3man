const FOREGROUND = { light: 'hsl(0, 0%, 3.9%)', dark: 'hsl(0, 0%, 98%)' };

/**
 * Renders the correct provider SVG icon with theme-aware foreground color.
 *
 * @example
 * <ProviderIcon provider="cloudflare-r2" size={20} />
 */
export function ProviderIcon({
  provider,
  size = 20,
  color,
}: {
  provider: S3Provider;
  size?: number;
  color?: string;
}) {
  const scheme = useColorScheme();
  const Comp = ICON_MAP[provider];
  const resolvedColor = color ?? (scheme === 'dark' ? FOREGROUND.dark : FOREGROUND.light);
  return <Comp size={size} color={resolvedColor} />;
}
